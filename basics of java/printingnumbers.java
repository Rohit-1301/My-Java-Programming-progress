public class printingnumbers {
    public static void main(String[] args) {
         int z=5;
        z--; //here z-- means z-1
        System.out.println("the value of z is "+z);
        System.out.println(4+3);
        System.out.println("4+3");
        System.out.println(4*3);
        System.out.println("4*3");
        System.out.println(4-3);
        System.out.println("4-3");
        System.out.println(16/4);
        System.out.println("16/4");
    }
}

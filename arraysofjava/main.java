package arraysofjava;

public class main {
    //add two numbers
     static int addtwo(int a,int b){
        return (a+b);
    } 
    
    //subtract one number from another number.  
    static int subtractonefromanothernumber(int a,int b) {
        return (a-b);
    }
    public static void main(String[] args) {
        System.out.println("The sum of the two numbers is: " + addtwo(5,6));
        
        System.out.println("\n\n");
        System.out.println("The difference of the two numbers is: " + subtractonefromanothernumber(5,6));
    }
}   // End of class main    

    

